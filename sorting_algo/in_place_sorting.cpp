//🔹 In-Place Sorting

// Sorting is done inside the original array

// No extra array is created

// Uses only constant extra space → O(1)

// Elements are swapped inside the same memory

// 🔹 Example

// Array before sorting
// [5, 3, 2, 4, 1]

// After in-place sort
// [1, 2, 3, 4, 5]

// The same array is modified — no new array is used.

//🔹 In-place vs Not In-place
// Type	Extra Memory	Example
// In-place	O(1)	    Bubble Sort, Selection Sort, Insertion Sort,
//                      Quick Sort

// Not In-place	O(n)	Merge Sort